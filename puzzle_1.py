'''
A salesman sold twice as much pears in the afternoon than in the morning.
If he sold 360 kilograms of pears that day, how many kilograms did he sell in the morning 
and how many in the afternoon?
'''

total_kg = 360

am_sales = total_kg / 3
pm_sales = 2 * am_sales

print(f"Morning sales: {am_sales}")
print(f"Afternoon sales: {pm_sales}")


